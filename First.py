name = "Suraj"
age = 19
pendingFees = True
GPA = 3.45
print(name, age, pendingFees, GPA)

# Type of each variable
print("Type of name", type(name))
print("Type of age", type(age))
print("Type of pendingFees", type(pendingFees))
print("Type of GPA", type(GPA))

# print the value of each variable
print(f"Value of name: {name}")
print(f"Value of age: {age}")
print(f"Value of pendingFees: {pendingFees}")
print(f"Value of GPA: {GPA}")

# print the value of each variable in a sentence
print(f"The string variable says: '{name}'")
print(f"The integer variable has value is : {age}")
print(f"The Boolean variable has value is : {pendingFees}")
print(f"The float variable has value is: {GPA}")

